    /// <summary>
    /// Returns the total advance width of <paramref name="text"/> in PDF points,
    /// correctly handling the full WinAnsiEncoding range (including accented Latin
    /// characters, curly quotes, em-dash, ellipsis, Euro sign, etc.).
    /// <para>
    /// Characters outside WinAnsiEncoding (e.g. CJK, Arabic) fall back to 500 units.
    /// </para>
    /// Font selection mirrors <c>TextBlock.Draw()</c>:
    /// bold+italic вҶ’ Times-BoldItalic (same widths as Times-Bold),
    /// bold only   вҶ’ Times-Bold,
    /// italic only вҶ’ Times-Italic,
    /// neither     вҶ’ Helvetica / Helvetica-Oblique (same widths).
    /// </summary>
    internal static double MeasureWidth(string text, double fontSize, bool bold, bool italic = false)
    {
        var table = (bold, italic) switch
        {
            (true,  _)     => _timesBoldWidths,
            (false, true)  => _timesItalicWidths,
            _              => _helveticaWidths,
        };
        double total = 0;
        foreach (char c in text)
        {
            int units;
            if (WinAnsiEncoding.TryGetByte(c, out byte b))
            {
                // b is the WinAnsi byte value (32вҖ“255); index into the width table.
                int index = b - 32;
                units = (index >= 0 && index < table.Length && table[index] != 0)
                        ? table[index]
                        : 500; // fallback for undefined positions (e.g. byte 127)
            }
            else
            {
                units = 500; // unmappable (CJK, Arabic, etc.) вҖ” substitution glyph
            }
            total += units * fontSize / 1000.0;
        }
        return total;
    }
