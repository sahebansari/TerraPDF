    internal static double MeasureWidth(string text, double fontSize, ResolvedFont font, bool bold, bool italic) =>
        font.IsCustom
            ? font.Custom!.MeasureWidth(text, fontSize)
            : MeasureWidth(text, fontSize, font.StandardFamily, bold, italic);
}
