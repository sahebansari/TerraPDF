    // Courier is monospaced: every glyph advances 600 units in all four variants.
    private const int _courierWidth = 600;

    // -- Public API -----------------------------------------------------------

    /// <summary>
    /// Returns the width table for a font variant, or <c>null</c> for Courier
    /// (monospaced — constant <see cref="_courierWidth"/> units per glyph).
    /// Oblique/italic Helvetica variants share the upright tables (identical
    /// advance widths in the Adobe AFMs).
    /// </summary>
    private static int[]? WidthTable(PdfFontFamily family, bool bold, bool italic) =>
        family switch
        {
            PdfFontFamily.Courier => null,
            PdfFontFamily.Times => (bold, italic) switch
            {
                (true,  true)  => _timesBoldItalicWidths,
                (true,  false) => _timesBoldWidths,
                (false, true)  => _timesItalicWidths,
                _              => _timesRomanWidths,
            },
            _ => bold ? _helveticaBoldWidths : _helveticaWidths,
        };

    /// <summary>
    /// Returns the total advance width of <paramref name="text"/> in PDF points
    /// for the given standard font family and weight/slant, correctly handling
    /// the full WinAnsiEncoding range (accented Latin characters, curly quotes,
    /// em-dash, ellipsis, Euro sign, etc.).
    /// <para>
    /// Characters outside WinAnsiEncoding (e.g. CJK, Arabic) fall back to 500 units.
    /// </para>
    /// </summary>
    internal static double MeasureWidth(string text, double fontSize,
        PdfFontFamily family, bool bold, bool italic)
    {
        int[]? table = WidthTable(family, bold, italic);
        double total = 0;
        foreach (char c in text)
        {
            int units;
            if (table is null)
            {
                units = _courierWidth;
            }
            else if (WinAnsiEncoding.TryGetByte(c, out byte b))
            {
                // b is the WinAnsi byte value (32–255); index into the width table.
                int index = b - 32;
                units = (index >= 0 && index < table.Length && table[index] != 0)
                        ? table[index]
                        : 500; // fallback for undefined positions (e.g. byte 127)
            }
            else
            {
                units = 500; // unmappable (CJK, Arabic, etc.) — substitution glyph
            }
            total += units * fontSize / 1000.0;
        }
        return total;
    }

